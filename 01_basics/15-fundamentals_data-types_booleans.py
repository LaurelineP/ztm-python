###################  BOOLEANS  ###################
# • Boolean in Python refers to "bool" built-in function
# • Can only be primitives value:
#   - True
#   - False

print(f'\nBOOLEANS: are primitive value of `True` or `False`')
isTrue = True
print(f' - `isTrue` variable returns the boolean value: {isTrue}')

isFalse = False
print(f' - `isFalse` variable returns the boolean value: {isFalse}')

booleanTransformation = bool(1)
print(f' - `bool(1)` returns a boolean value {booleanTransformation}')
