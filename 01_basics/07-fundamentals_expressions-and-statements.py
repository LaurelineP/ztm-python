# Expressions are the right side of a variable
print('\n1 - EXPRESSIONS:\n  represents the right side of a variable \n  ( after the equal ), \n    --> ex \'variable="something"\',\n   the expression is \'something\'')


# Statements are the entire line
print('\n\n\n1 - STATEMENTS:\n  represents the entire line \n   --> ex \'variable="something"\',\n  the statement is the entire \n  \'variable="something"\'')